// Aggregate stats
export function getVendorStats() {
  const totalAnnual = vendors.reduce((sum, v) => sum + v.annualSpend, 0)
  const totalMonthly = vendors.reduce((sum, v) => sum + v.monthlySpend, 0)
  const vendorCount = vendors.length
  const headcount = 35
  const costPerEmployee = Math.round(totalAnnual / headcount)

  const byCategory = {}
  for (const v of vendors) {
    if (!byCategory[v.category]) byCategory[v.category] = { spend: 0, count: 0 }
    byCategory[v.category].spend += v.annualSpend
    byCategory[v.category].count += 1
  }

  const upcomingRenewals = vendors
    .filter(v => v.renewalDate)
    .map(v => ({ ...v, daysUntil: daysUntil(v.renewalDate) }))
    .filter(r => r.daysUntil > 0 && r.daysUntil <= 90)
    .sort((a, b) => a.daysUntil - b.daysUntil)

  const criticalVendors = vendors.filter(v => v.health < 40)
  const attentionVendors = vendors.filter(v => v.health >= 40 && v.health < 60)

  return {
    totalAnnual,
    totalMonthly,
    vendorCount,
    headcount,
    costPerEmployee,
    byCategory,
    upcomingRenewals,
    criticalVendors,
    attentionVendors,
  }
}

